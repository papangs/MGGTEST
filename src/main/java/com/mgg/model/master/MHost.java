package com.mgg.model.master;

public class MHost {
	
	private String kd_host;
	private String host_desc;
	private Integer product_id;
	public String getKd_host() {
		return kd_host;
	}
	public void setKd_host(String kd_host) {
		this.kd_host = kd_host;
	}
	public Integer getProduct_id() {
		return product_id;
	}
	public void setProduct_id(Integer product_id) {
		this.product_id = product_id;
	}
	public String getHost_desc() {
		return host_desc;
	}
	public void setHost_desc(String host_desc) {
		this.host_desc = host_desc;
	}
}
